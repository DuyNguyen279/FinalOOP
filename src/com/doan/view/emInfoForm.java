    private void searchBoxKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchBoxKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() != KeyEvent.VK_ENTER) {
            return;
        }
        if (searchBox.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập thông tin tìm kiếm");
            return;
        }
        String key1 = LuaChon.getSelectedItem().toString();
        String key2 = searchBox.getText();
        search searchInstance = new search().getInstance();

        List<NhanVien> list = new ArrayList<>();

        switch (key1) {
            case "Mã Nhân Viên":
                NhanVien nv = searchInstance.searchEmployeeById(key2);
                if (nv != null) {
                    list.add(nv);
                }
                break;
            case "Tên Nhân Viên":
                list = searchInstance.searchEmployeeByName(key2);
                break;
        }

        if (!list.isEmpty()) {
            setTableData(list);
        } else {
            JOptionPane.showMessageDialog(null, "Không tìm thấy nhân viên nào");
        }
    }//GEN-LAST:event_searchBoxKeyPressed

