    private void createdBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createdBtnActionPerformed
        // TODO add your handling code here:
        String idEmployee = newIdEmployee.getText();
        String nameEmployee = newNameEmployee.getText();
        int ageEmployee = Integer.parseInt(newAgeEmployee.getText());
        boolean genderEmployee = newGenderEmployee.getSelectedItem().toString().equals("Nam");
        String addressEmployee = newAddressEmployee.getText();
        String phoneEmployee = newPhoneEmployee.getText();
        double salaryEmployee = Double.parseDouble(newSalaryEmployee.getText());
        String emailEmployee = newEmailEmployee.getText();
        String idAccount = new generateNewId().getInstance().generateNewIdAccount();
        String nameAccount = newNameAcc.getText();
        String username = newUsername.getText();
        String password = newPassword.getText();
        String confirmPassword = ComfirmPassword.getText();

        if (password.equals(confirmPassword)) {
            NhanVien nv = new NhanVien(idEmployee, nameEmployee, ageEmployee,genderEmployee,addressEmployee,phoneEmployee,salaryEmployee);
            new addNew().addNewEmployee(idEmployee, nameEmployee, ageEmployee,genderEmployee,addressEmployee,phoneEmployee,salaryEmployee);
            new addNew().addNewAccount(idAccount, nameAccount, username, password, "Employee", true, emailEmployee, nv);
            JOptionPane.showMessageDialog(null, "Thêm nhân viên và tài khoản thành công");
            addAccount.setVisible(false);
            setTableData(new NhanVienDAO().getInstance().selectAll());
        } else {
            JOptionPane.showMessageDialog(null, "Mật khẩu không khớp");
        }
    }//GEN-LAST:event_createdBtnActionPerformed

