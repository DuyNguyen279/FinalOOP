    public void setTableData(List<NhanVien> all) {
        model.setRowCount(0);
        for (NhanVien e : all) {
            String gt;
            if (e.getGioiTinh()){
                gt = "Nam";
            } else gt = "Nữ";
            if (!e.isIs_delete()){
                model.addRow(new Object[]{
                    e.getMaNV(),
                    e.getTenNV(),
                    e.getTuoi(),
                    gt
                });
            }   
        }
    }

