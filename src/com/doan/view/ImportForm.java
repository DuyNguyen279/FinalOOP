    private void ImportBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ImportBtnActionPerformed
        // TODO add your handling code here:
        String maPhieuNhap = txtIdGrn.getText().toString();
        NCC ncc = NCCDAO.getInstance().selectByTenNCC(listSupplier.getSelectedItem().toString());
        NhanVien nv = NhanVienDAO.getInstance().selectById(txtIdImportEmployee.getText().toString());
        Date ngayNhap = Date.valueOf(LocalDate.now());
        List<SanPham> sp = new ArrayList<SanPham>();
        for (int i = 0; i < model2.getRowCount(); i++){
            SanPham x = new SanPhamDAO().getInstance().selectById(model2.getValueAt(i, 0).toString());
            String newIdItem = new generateNewId().getInstance().generateNewIdItem();
            x.setMaSP(newIdItem);
            x.setGiaNhap(Double.valueOf(model2.getValueAt(i, 3).toString()));
            x.setSoLuong(Integer.valueOf(model2.getValueAt(i, 2).toString()));

            nameItem.setText(x.getTenSP());
            capnhat.setLocationRelativeTo(null);
            capnhat.setVisible(true);  
  
            x.setNSX(Date.valueOf(createdDay.getText().toString()));
            x.setHSD(Date.valueOf(ExpiredDay.getText().toString()));

            if (x.getNSX().after(x.getHSD())){
                JOptionPane.showMessageDialog(null, "Ngày sản xuất không được lớn hơn ngày hết hạn", "Cảnh Báo", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            sp.add(x);
            new SanPhamDAO().getInstance().insert(x);
        }
        double tongTien = Double.valueOf(txtTotalPrice.getText().toString());
        addNew.getInstance().addNewGrn(maPhieuNhap, ncc, nv, ngayNhap, sp, tongTien);
        JOptionPane.showMessageDialog(null, "Thêm hàng thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        
        model.setRowCount(0);
        model2.setRowCount(0);

        for (SanPham x : new SanPhamDAO().getInstance().selectAll()) {
            model.addRow(new Object[]{
                x.getMaSP(),
                x.getTenSP(),
                x.getSoLuong(),
                x.getGiaNY()
            });
        }
    }//GEN-LAST:event_ImportBtnActionPerformed

    private void editQuantityItemFormGrnBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editQuantityItemFormGrnBtnActionPerformed
        // TODO add your handling code here:
        int i_row = bangNhap.getSelectedRow();
        
        if (i_row == -1){
            JOptionPane.showMessageDialog(null,"Vui lòng chọn sản phẩm","Cảnh Báo",JOptionPane.WARNING_MESSAGE);
            return;
        }
        int sl;
        do {
            sl = Integer.valueOf(JOptionPane.showInputDialog(null,"Vui lòng nhập số lượng sản phẩm","Sửa số lượng sản phẩm",JOptionPane.OK_OPTION));
            if (sl < 0) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập số lượng từ 0 trở lên", "Cảnh Báo", JOptionPane.WARNING_MESSAGE);
            }
        } while (sl < 0);

        if (sl == 0){
            model2.removeRow(i_row);
            autoCalculate();
            return;
        }
        
        bangNhap.setValueAt(sl, i_row, 2);
        bangNhap.setValueAt(sl * Double.valueOf(bangNhap.getValueAt(i_row, 3).toString()), i_row, 4);
        autoCalculate();
        
    }//GEN-LAST:event_editQuantityItemFormGrnBtnActionPerformed

