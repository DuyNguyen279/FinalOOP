    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        String id = txtIdProduct.getText();
        String nameItem = txtNameProduct.getText();
        double price = Double.valueOf((txtPriceProduct.getText()));
        double importPrice = Double.valueOf((txtImportPriceProduct.getText()));
        int quantity = Integer.valueOf(txtQuantityProduct.getText());
        String type = String.valueOf(txtLoaiSanPham.getSelectedItem());

        SanPham sp = new SanPham(id, nameItem, price, importPrice, quantity, Date.valueOf(LocalDate.now()), Date.valueOf(LocalDate.now()), type);
        for(int i = 0; i < model2.getRowCount(); i++){
            if (model2.getValueAt(i, 0).toString().equals(id)){
                Integer number = Integer.valueOf(id.substring(2)) + 1;
                id = "SP" + number.toString();
            }
        }
        model2.addRow(new Object[]{
            id,
            nameItem,
            quantity,
            importPrice,
            quantity * importPrice
        });
        addItem.setVisible(false);
    }//GEN-LAST:event_addBtnActionPerformed

